async function importTemplates() {
    console.log(`Starting import of ${templates.length} templates...`);

    let count = 0;
    for (const t of templates) {
        const contentPayload = JSON.stringify({ text: t.text });

        // Check if exists by title
        const { data: existing } = await supabase
            .from('website_content')
            .select('id')
            .eq('section', 'chat_templates')
            .eq('title', t.title)
            .maybeSingle();

        if (existing) {
            console.log(`Updated: ${t.title}`);
            await supabase
                .from('website_content')
                .update({ content: contentPayload })
                .eq('id', existing.id);
        } else {
            console.log(`Created: ${t.title}`);
            await supabase
                .from('website_content')
                .insert({
                    section: 'chat_templates',
                    title: t.title,
                    content: contentPayload,
                    is_active: true
                });
        }
        count++;
    }

    console.log(`Import finished. Processed ${count} templates.`);
}

importTemplates().catch(console.error);
